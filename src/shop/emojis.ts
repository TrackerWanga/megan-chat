export function getAllEmojis() {
  return Object.entries(EMOJIS).map(([category, emojis]) => ({
    category: category.charAt(0).toUpperCase() + category.slice(1),
    emojis,
    count: emojis.length,
  }));
}

export function getEmojiCount(): number {
  return Object.values(EMOJIS).reduce((sum, arr) => sum + arr.length, 0);
}

